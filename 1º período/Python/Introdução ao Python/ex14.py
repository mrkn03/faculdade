'''Faça um algoritmo onde devera ser informado a base e a altura de um triangulo. o algoritmo devera exibir o valor da area'''

base=float(input('Informe a base do triangulo em centimetros:  '))
altura=float(input('Informe a altura do triangulo em centimetros:  '))
area=(base*altura)/2
print('A área deste triangulo é igual a {}cm'.format(area))
